
public class ArrayExample10 {

	public static void main(String[] args) {
		String a="Good Begining Makes Good Ending";
		String b[]=a.split(" ");
	for(int i=0;i<b.length;i++){
		for(int j=i+1;j<b.length;j++) {
			if(b[i].equals(b[j])) {
		
			System.out.println(b[i]+" :duplicate word");

	}

}


	}
	}
}
	


