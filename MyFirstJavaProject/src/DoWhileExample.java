
public class DoWhileExample {

	public static void main(String[] args) {
		int i=11;
		do {
			System.out.println(i);
		    i++;
			
		}while(i<=10);
		System.out.println("x----------------x");
		int j=1;
		do {
			System.out.println(j);
		    j++;
		}while(j<=10);
				

	}

}
